// 1. Using Single Quotes 

let singleQuotesStr = 'String With Single Quotes';
console.log(singleQuotesStr);

// 2. Using Double Quotes 

let doubleQuotesStr = "String with Double Quotes";
console.log(doubleQuotesStr);

// 3. String Constructor 

let strConstructor = new String('String with String constructor');
console.log(strConstructor);

// 4. Using Template Literal (String Interpolation)

let str = 'Ashish Nautiyal'
let stringInterpolation = `My name is ${str}`;
console.log(stringInterpolation);

// 5. Empty String 

let emptySingleStr = '';
let emptyDoubleStr = "";
console.log(emptySingleStr, emptyDoubleStr);

// 6. Multiple Strings (ES6 and later)

let multiString = `My name is Ashish Nautiyal 
    I am 20 years old,
        I am studying string.`
console.log(multiString);